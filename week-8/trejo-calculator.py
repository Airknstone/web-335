# adds 2 numbers and returns results
def add(n1, n2):
    return n1 + n2

# subtacts 2 numbers and returns results
def substract(n1, n2):
    return n1 - n2

# divides 2 numbers and returns results
def divide(n1,n2):
    return n1 / n2

# Calls each functions and prints results to console
print(add(10,5))
print(substract(40,3))
print(divide(3,6))